    //Fungsi Mencari File
    void cariFile() {
        File files;
        JFileChooser getChooser = new JFileChooser(".");
        getChooser.setFileFilter(new FileNameExtensionFilter(".wav", ".mp3", "wav", "mp3"));
        int buka_dialog = getChooser.showOpenDialog(Musik.this);
        if (buka_dialog == JFileChooser.APPROVE_OPTION) {
            files = getChooser.getSelectedFile();
            filePath = files.getAbsolutePath();
            String fileName = files.getName();
            try {
                fileExt = fileName.substring(fileName.lastIndexOf("."), fileName.length());
                musicName.setText(fileName);

                if (".wav".equals(fileExt)) {

                    AIS
                            = AudioSystem.getAudioInputStream(new File(filePath).getAbsoluteFile());
                    // create clip reference 
                    clip = AudioSystem.getClip();

                    // open audioInputStream to the clip 
                    clip.open(AIS);
                } else {
                    player.addToPlayList(getChooser.getSelectedFile());

                }
            } catch (Exception e) {
                e.printStackTrace();
            }

        }
    }

